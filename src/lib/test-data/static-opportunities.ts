/**
 * Helper function to get all static opportunities
 * This ensures consistent data across all test sessions
 */
export function getStaticTestOpportunities(): Opportunity[] {
  return [...STATIC_TEST_OPPORTUNITIES] // Return a copy to prevent mutations
}

/**
 * Helper function to get opportunities by state for testing geographic filters
 */
export function getOpportunitiesByState(state: string): Opportunity[] {
  return STATIC_TEST_OPPORTUNITIES.filter(opp => 
    opp.state === state || opp.performanceState === state
  )
}

/**
 * Helper function to get opportunities by set-aside type for testing set-aside filters
 */
export function getOpportunitiesBySetAside(setAsideType: SetAsideType): Opportunity[] {
  return STATIC_TEST_OPPORTUNITIES.filter(opp => opp.setAsideType === setAsideType)
}

/**
 * Helper function to get opportunities by NAICS code for testing NAICS filters
 */
export function getOpportunitiesByNaics(naicsCode: string): Opportunity[] {
  return STATIC_TEST_OPPORTUNITIES.filter(opp => 
    opp.naicsCodes?.includes(naicsCode) || opp.primaryNaics === naicsCode
  )
}